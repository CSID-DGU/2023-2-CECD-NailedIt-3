abstract class UserRemoteProvider {
  /* ------------------------------------------------------------ */
  /* -------------------------- Exists -------------------------- */
  /* ------------------------------------------------------------ */
  Future<bool> existsUser();

  /* ------------------------------------------------------------ */
  /* -------------------------- Getter -------------------------- */
  /* ------------------------------------------------------------ */
  Future<String> getId();

  Future<String> getNickname();

  Future<bool> getAllowedNotification();

  /* ------------------------------------------------------------ */
  /* -------------------------- Setter -------------------------- */
  /* ------------------------------------------------------------ */
  Future<void> setAllowedNotification(bool allowedNotification);

  Future<void> setDeviceToken(String token);
}
